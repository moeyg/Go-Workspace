// 🅐 ==
// 🅑 <=
// 🅒 >=
// 🅓 !=
// 🅔 <
// 🅕 >

// Now print each of the variables

package main

import "fmt"

func ex07() {
	fmt.Println(90 == 90) // true
	fmt.Println(90 <= 100) // true
	fmt.Println(90 >= 100) // false
	fmt.Println(90 != 90) // false
	fmt.Println(90 < 100) // true
	fmt.Println(90 > 100) // false
}